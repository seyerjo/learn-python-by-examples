"""Demonstrate basic for loop iteration in Python.

Shows how to iterate over a list of numbers using a for loop.
"""


def main():
    """Main function demonstrating for loop usage."""
    # Create a list of numbers
    numbers = [1, 2, 3, 4, 5]

    # Iterate through each number in the list
    for number in numbers:
        print(f"Current number: {number}")

    print()  # Final newline


if __name__ == "__main__":
    main()
