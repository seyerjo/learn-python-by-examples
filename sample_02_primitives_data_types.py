"""
Let's look at the data types and data structures we can work with in Python.
"""

# Variables with data types.
variable_integer = 1
variable_float = 3.14
variable_complex = 1 + 2j
variable_string = "Hello, Coder!"
variable_list = [1, 2, 3]
variable_tuple = (4.0, 5.5, 6.9)
variable_dictionary = {"key": "value"}
variable_set = {7, 8, 9}
variable_boolean = False
variable_none = None

# The 'type' function returns the data type.
print(type(variable_integer))  # Integer number.
print(type(variable_float))  # Float number.
print(type(variable_complex))  # Complex number.
print(type(variable_string))  # String of characters.
print(type(variable_list))  # List of elements.
print(type(variable_tuple))  # Tuple of elements.
print(type(variable_dictionary))  # Dictionary of key-value pairs.
print(type(variable_set))  # Set of unique elements.
print(type(variable_boolean))  # Boolean value.
print(type(variable_none))  # NoneType object.
